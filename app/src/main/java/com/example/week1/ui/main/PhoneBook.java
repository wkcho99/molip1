package com.example.week1.ui.main;

public class PhoneBook{
    private String id;
    private String name;
    private String tel;

    public String getName() {
        return this.name;
    }
    public void setName(String Name) {
        this.name = Name;
    }

    public String getTel() {
        return this.tel;
    }

    public void setTel(String Tel) {
        this.tel = Tel;
    }
    public String getId(){
        return this.id;
    }
}

